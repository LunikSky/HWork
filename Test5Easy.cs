public class Test5Easy
{
	 public void DefiningLargerNumber(int aNumber, int bNumber)
	{      
        if (aNumber > bNumber)
        {
            Console.WriteLine($"Число а:{aNumber}больше числа b{bNumber}");
        }
        else
        {
            Console.WriteLine($"Число b:{bNumber}больше числа a{aNumber}");
        }
    }
}
